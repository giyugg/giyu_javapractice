public class lesson9_real_life_example {
    public static void main(String[] args) {
        
        // for the last activity regarding Java data types, I will be performing an In real life situation.
        // I will sell an item and put the data about that product.
        String itemName = "Gatorade";
        float price = 47.99f;
        int items = 4;
        float totalCost = price * items;
        char currency = 'P';

        System.out.println("Product Name: " + itemName);
        System.out.println("Number of items: " + items);
        System.out.println("Cost per item: " + currency + price);
        System.out.println("Total Bill: " + currency + totalCost);
    }
}
